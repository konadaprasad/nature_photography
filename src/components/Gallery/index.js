class Gallery extends Component {
  state = {
    isActive: true,
    imgUrl: imagesList[0].imageUrl,
    imageAlt: imagesList[0].imageAltText,
  }

  stateChange = (imageUrl, imageAltText, isActive) => {
    this.setState({imgUrl: imageUrl, imageAlt: imageAltText, isActive: false})
  }

  render() {
    const {imgUrl, imageAlt, isActive} = this.state

    return (
      <div className="container">
        <div className="inner-container">
          <img src={imgUrl} alt={imageAlt} className="image" />
        </div>
        <div className="content-container">
          <h1 className="heading">Nature Photography</h1>
          <p className="para">Nature Photography by Rahul</p>
        </div>
        <ul className="image-container">
          {imagesList.map(eachItem => (
            <ThumbnailItem
              List={eachItem}
              key={eachItem.id}
              stateChange={this.stateChange}
              isActive={isActive}
            />
          ))}
        </ul>
      </div>
    )
  }
}
